Surveiller les annonces
Supprimez toutes les annonces qui violent la loi ou placez-les dans une catégorie ou une ville fausse ou vulgaire qui enfreint sa charte.
Il supprimera également toute annonce comportant une promotion ou un contenu incompris.
Annonces multiples et doublons
De nombreuses annonces ne sont pas autorisées pour le même article ou service. Supprimez l'ancienne annonce, attendez 5 jours, puis publiez votre nouvelle annonce. De même, il est interdit de publier des annonces sur le même article ou le même service dans différentes catégories ou régions.
Titre de l’annonce
Le titre de l'annonce doit décrire brièvement l'article ou le service proposé. Ne doit pas contenir de numéros de téléphone ou de termes tels que:
 <li>urgent</li>
 <li>e vends, vends, vends, offre</li>
 <li>demande</li>
 <li>histoire</li>
 <li>Nom de l'entreprise</li>
 <li>Lien de réseau social ou compte</li>
Se réserve le droit de modifier le titre de l'annonce pour se conformer aux règles.
Texte de l'annonce:
L'article ou le service doit être décrit dans un texte d'annonce dédié à un seul produit. La description du produit ne peut pas contenir d'autres informations (web,Lien de réseau social ou compte  etc.).
Nombre d'annonces gratuites par utilisateur:
Le nombre d'annonces gratuites par utilisateur est limité par type d'utilisateur (privé / magasin).
